//Inicializacion de variables fundamentales para el funcionamiento
(SETUP)
    @16384
    D = A 
    @KIRBOPOS
    M = D
    @HOOK
    M = 1
	@HOOKSTOP
    M = 1
    @LOOP
    0;JMP
//Ciclo infinito que esta continuamente checkeando el teclado que presiono el teclado
(LOOP)
    @KEYREAD
    0;JMP
    @LOOP
    0;JMP

//Funcion para comprobar si el usuario solto alguna de las teclas para poder continuar dibujando
(UNPRESSKEY)
    @24576
    D = M
    @KEYTYPE
    D = M - D
    @LOOP
    D;JNE
    @UNPRESSKEY
    0;JMP

//Lectura de teclado y deteccion de tipo de tecla presionada(UP,DOWN,LEFT,RIGHT)
(KEYREAD)
    @24576
    D = M
    @132
    D = D - A
    @READRIGHTKEY//procesado de tecla derecha
    D;JEQ
    @24576
    D = M
    @130
    D = D - A
    @READLEFTKEY//procesado de tecla izquierda
    D;JEQ
	@24576
    D = M
    @131
    D = D - A
    @READUPKEY //procesado de tecla arriba
    D;JEQ
	@24576
    D = M
    @133
    D = D - A
    @READDOWNKEY //procesado de tecla abajo
    D;JEQ
	@24576
    D = M
    @129
    D = D - A
    @SETUPSTOP//Si se presiona backspace se acaba el programa
    D;JEQ
    @LOOP
    0;JMP

//inicializacion de variables para el setup Stop
(SETUPSTOP)
	@HOOKSTOP
	M = 0
	@SETUPCLS

(READRIGHTKEY)
	@132
    D = A 
    @KEYTYPE
    M = D

    @HOOKKEY//Hook de que se presiono tecla de tipo derecha
    M=0
    @HOOK
    D = M
    @SETUPCLS//limpiar pantalla
    D;JGT

(READLEFTKEY)
	@130
    D = A 
    @KEYTYPE
    M = D

	@HOOKKEY//Hook de que se presiono tecla de tipo izquierda
    M=-1
    @HOOK
    D = M
    @SETUPCLS
    D;JGT

(READUPKEY)
	@131
    D = A 
    @KEYTYPE
    M = D

	@HOOKKEY
    M=1
    @HOOK//Hook de que se presiono tecla de tipo arriba
    D = M
    @SETUPCLS
    D;JGT

(READDOWNKEY)
	@133
    D = A 
    @KEYTYPE
    M = D

	@R2
	D = A
    @HOOKKEY//Hook de que se presiono tecla de tipo abajo
    M = D
    @HOOK
    D = M
    @SETUPCLS
    D;JGT

//Funcion para detectar que tipo de dibujo o sprint se tiene que colocar en la VRAM segun lo que presione el usuario
//Se encarga tanto de tecla LEFT, RIGHT
(MOVEMENT)
	@HOOKSTOP//Deteccion y comprobacion para indicar si el programa se detuvo o no
    D = M
	@STOP
	D;JEQ
	@HOOK
    M = 1
	@HOOKKEY
	D = M
	@RIGHT//Ejecucion de SPRINT RIGHT
	D;JEQ
	@HOOKKEY
	D=M
	@LEFT//Ejecucion de SPRINT LEFT
	D;JLT
	@HOOKKEY
	D = M
	@MOVEMENT2//Si no es tecla tipo derecha o izquierda ejecutar MOVEMENT2
	D;JGT
	@LOOP
	0;JMP

//Funcion para detectar que tipo de dibujo o sprint se tiene que colocar en la VRAM segun lo que presione el usuario
//Se encarga tanto de tecla UP, DOWN
(MOVEMENT2)
	@HOOKKEY
	D=M
	D = D - 1
	@UP//Ejecucion de SPRINT UP
	D;JEQ 
	@HOOKKEY
	D=M
	@R2 
	D = D - A
	@DOWN//Ejecucion de SPRINT DOWN
	D;JEQ 


(RIGHT) 
	@KIRBOPOS
	MD=M + 1//Aumenta 1 registro para dibujar el sprint
	@KirboRight
	0;JMP

(LEFT) 
	@KIRBOPOS
	MD=M - 1//Disminuye 1 registro para dibujar el sprint
	@KirboLeft
	0;JMP

(DOWN) 
	@320
	D = A
	@KIRBOPOS
	MD=M + D//Aumenta 320 registros para dibujar el sprint se pone 320 para que sea un movimiento mas rapido
	@KirboDown
	0;JMP

(UP) 
	@320
	D = A
	@KIRBOPOS
	MD=M - D//Disminuye 320 registros para dibujar el sprint se pone 320 para que sea un movimiento mas rapido
	@KirboUp
	0;JMP

//Preparacion de estados o pointer fundamentales para el borrado
(SETUPCLS)
    @Pointer
    M = 0
    @CLS

//Borrado completo de pantalla para refrescar la imagen
(CLS)
// while Pointer<8191 goto stop
// RAM[Pointer+16384] = 0
    @HOOK//Establecer Hook de borrado en 0 para indicar que ya se refresco la imagen
    M = 0
    @8191
    D = A
    @Pointer
    D = M - D//if RAM[Pointer]>8191 go to movement -> pantalla limpiada
    @MOVEMENT
    D;JGT

    //RAM[Pointer+16834] do clear 16 bits
    @Pointer
    D = M
    @16384
    A = A + D
    M = 0
    //Pointer increase +1 registrer
    @Pointer
    M = M+1

    @CLS   
    0;JMP


